# Необходимоотсортироватьпервыедветретисписка 
# в порядке возрастания, если среднее арифметическое 
# всехэлементовбольшенуля;иначе—лишьпервуютреть. 
# Остальную часть списка не сортировать, а расположить 
# в обратном порядке.


def process_list(lst):
    if not lst:  # Обработка пустого списка
        return lst
    
    # Вычисляем среднее арифметическое
    avg = sum(lst) / len(lst)
    
    n = len(lst)
    
    # Определяем, какую часть сортировать
    if avg > 0:
        # Сортируем первые две трети
        third = n // 3
        sort_end = 2 * third  # Конец сортируемой части
    else:
        # Сортируем только первую треть
        third = n // 3
        sort_end = third  # Конец сортируемой части
    
    # Сортируем нужную часть в порядке возрастания
    sorted_part = sorted(lst[:sort_end])
    
    # Оставшуюся часть разворачиваем в обратном порядке
    remaining_part = lst[sort_end:][::-1]
    
    # Собираем результат
    result = sorted_part + remaining_part
    
    return result

# Примеры использования:
if __name__ == "__main__":
    # Пример 1: среднее > 0 → сортируем 2/3
    list1 = [9, 3, 7, 1, 5, 8, 2, 6, 4]
    print("Исходный список 1:", list1)
    print("Результат 1:", process_list(list1))
    
    # Пример 2: среднее ≤ 0 → сортируем 1/3
    list2 = [-5, -2, -8, 3, 1, -4]
    print("\nИсходный список 2:", list2)
    print("Среднее арифметическое:", sum(list2) / len(list2))
    print("Результат 2:", process_list(list2))
    
    # Пример 3: пустой список
    list3 = []
    print("\nПустой список:", process_list(list3))
